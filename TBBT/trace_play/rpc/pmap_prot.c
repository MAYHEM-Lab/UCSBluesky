#include "rpc/types.h"
#include "rpc/xdr.h"
#include "rpc/pmap_prot.h"


bool_t
xdr_pmap(
	XDR *xdrs,
	struct pmap *regs)
{

	if (xdr_uint32_t(xdrs, &regs->pm_prog) && 
		xdr_uint32_t(xdrs, &regs->pm_vers) && 
		xdr_uint32_t(xdrs, &regs->pm_prot))
		return (xdr_uint32_t(xdrs, &regs->pm_port));
	return (FALSE);
}
